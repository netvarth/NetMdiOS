#import "SyncErrorDTO.h"

@implementation SyncErrorDTO

@synthesize errCode,params,displayError;

- (id)initWithError:(NSString*)ErrorCode
             params:(NSArray*)Params
       displayError:(BOOL)DisplayError {
    
    self    = [super init];
    if (self != nil) {
        errCode    = ErrorCode;
        params         = Params;
        displayError   = DisplayError;
    }
    
    return self;
}


@end
