#import "Expression.h"

@implementation Expression

- (id)initWithExpressions:(NSString*)name
                    value:(NSString*)value
              expOperator:(NSString*)expOperator {
    
    self    = [super init];
    if (self != nil) {
        self.name           = name;
        self.value          = value;
        self.expOperator    = expOperator;
    }
    
    return self;
}

@end
