#import "Filter.h"

@implementation Filter

- (id)initWithFilter:(NSArray*)expList
                from:(int)from
               count:(int)count
                 asc:(BOOL)asc {
    
    self    = [super init];
    if (self != nil) {
        self.expList    = expList;
        self.from       = from;
        self.count      = count;
        self.asc        = asc;
    }
    
    return self;
}

@end
